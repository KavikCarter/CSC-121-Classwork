# 4-2. Animals 

# Think of at least three animals that share a common characteristic.
# Common characteristic: they are all popular household pets.

animals = ["dog", "cat", "rabbit"]

# Print just the name of each animal.
for animal in animals:
    print(animal)

print()  # blank line for readability

# Print a statement about each animal.
for animal in animals:
    print(f"A {animal} would make a great pet.")

print()  # blank line for readability

# Print what they all have in common.
print("Any of these animals would make a great pet!")